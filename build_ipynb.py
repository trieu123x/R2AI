            "    def expand_to_parent_article(self, results: list) -> list:\n",
            "        \"\"\"Parent Retrieval: L\u1ea5y to\u00e0n b\u1ed9 n\u1ed9i dung c\u1ee7a \u0110i\u1ec1u lu\u1eadt t\u1eeb c\u00e1c chunk ban \u0111\u1ea7u.\"\"\"\n",
            "        if not results:\n",
            "            return []\n",
            "            \n",
            "        target_articles = {}\n",
            "        hit_indexes = {}\n",
            "        for r in results:\n",
            "            if not r.article_hint:\n",
            "                continue\n",
            "            doc_id = r.document_id\n",
            "            if doc_id not in target_articles:\n",
            "                target_articles[doc_id] = set()\n",
            "            target_articles[doc_id].add(r.article_hint)\n",
            "            \n",
            "            # Ghi nh\u1edb v\u1ecb tr\u00ed chunk_index \u0111\u00e3 hit\n",
            "            key = (doc_id, r.article_hint)\n",
            "            if key not in hit_indexes:\n",
            "                hit_indexes[key] = set()\n",
            "            hit_indexes[key].add(r.chunk_index)\n",
            "            \n",
            "        if not target_articles:\n",
            "            return results\n",
            "            \n",
            "        doc_ids = list(target_articles.keys())\n",
            "        conn = self._get_conn()\n",
            "        cur = conn.cursor()\n",
            "        \n",
            "        placeholders = \",\".join(\"?\" for _ in doc_ids)\n",
            "        cur.execute(f\"\"\"\n",
            "            SELECT document_id, chunk_index, content\n",
            "            FROM document_chunks\n",
            "            WHERE document_id IN ({placeholders})\n",
            "            ORDER BY document_id, chunk_index\n",
            "        \"\"\", doc_ids)\n",
            "        \n",
            "        all_chunks = cur.fetchall()\n",
            "        \n",
            "        doc_chunks = {}\n",
            "        for row in all_chunks:\n",
            "            did = row[\"document_id\"]\n",
            "            if did not in doc_chunks:\n",
            "                doc_chunks[did] = []\n",
            "            doc_chunks[did].append(row)\n",
