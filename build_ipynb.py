            "    def retrieve(\n",
            "        self,\n",
            "        query: str,\n",
            "        mode: Literal[\"vector\", \"fts\", \"hybrid\"] = \"fts\",\n",
            "        top_k: Optional[int] = None,\n",
            "        rerank: bool = True,\n",
            "    ) -> List[RetrievalResult]:\n",
            "        t0 = time.time()\n",
            "        k = top_k or self.top_k\n",
            "        \n",
            "        # 1. Query Expansion (Intent Understanding)\n",
            "        expanded_query = self.expand_query(query)\n",
            "        \n",
            "        # 2. Truy xu\u1ea5t danh s\u00e1ch \u1ee9ng vi\u00ean (candidate pool)\n",
            "        if rerank:\n",
            "            fetch_k = 50\n",
            "        else:\n",
            "            fetch_k = max(50, k * 4)\n",
            "        \n",
            "        if mode == \"vector\":\n",
            "            results = self.vector_search(expanded_query, top_k=fetch_k)\n",
            "        elif mode == \"fts\":\n",
            "            results = self.fts_search(expanded_query, top_k=fetch_k)\n",
            "        else:\n",
            "            results = self.hybrid_search(expanded_query, top_k=fetch_k)\n",
            "            \n",
            "        # 3. Ti\u1ebfn h\u00e0nh l\u1ecdc tr\u00f9ng l\u1eb7p tr\u01b0\u1edbc \u0111\u1ec3 lo\u1ea1i b\u1ecf nhi\u1ec5u\n",
            "        unique_results = []\n",
            "        for r in results:\n",
            "            is_dup = False\n",
            "            for ur in unique_results:\n",
            "                if self.are_chunks_duplicate(r, ur):\n",
            "                     is_dup = True\n",
            "                     break\n",
            "            if not is_dup:\n",
            "                unique_results.append(r)\n",
            "                \n",
            "        # 4. Parent Retrieval (Thay th\u1ebf cho aggregate_chunks_into_articles)\n",
            "        article_results = self.expand_to_parent_article(unique_results)\n",
            "\n",
            "        # 5. Rerank v\u00e0 l\u1ecdc\n",
            "        if rerank:\n",
            "            results = self.rerank(query, article_results, top_k=None)\n",
            "        else:\n",
            "            article_results.sort(key=lambda x: x.score, reverse=True)\n",
            "            results = article_results\n",
            "\n",
            "        # 4. \u00c1p d\u1ee5ng Absolute & Relative Score Thresholds (Dynamic Thresholding)\n",
            "        if results:\n",
            "            RERANK_THRESHOLD = 0.15\n",
            "            best_score = results[0].score\n",
            "            \n",
            "            relative_threshold_ratio = 0.5\n",
            "            \n",
            "            # Dynamic gap filtering: N\u1ebfu top 1 v\u01b0\u1ee3t tr\u1ed9i ho\u00e0n to\u00e0n top 2\n",
            "            if len(results) > 1:\n",
            "                score_1 = results[0].score\n",
            "                score_2 = results[1].score\n",
            "                if (score_1 - score_2) > 0.2:\n",
            "                    relative_threshold_ratio = 0.8\n",
            "            \n",
            "            filtered_by_score = []\n",
            "            for r in results:\n",
            "                # Gi\u1eef chunk n\u1ebfu th\u1ecfa m\u00e3n ng\u01b0\u1ee1ng tuy\u1ec7t \u0111\u1ed1i v\u00e0 ng\u01b0\u1ee1ng t\u01b0\u01a1ng \u0111\u1ed1i \u0111\u1ed9ng\n",
            "                if r.score >= RERANK_THRESHOLD and r.score >= best_score * relative_threshold_ratio:\n",
            "                    filtered_by_score.append(r)\n",
            "            results = filtered_by_score\n",
            "\n",
            "        # 5. Article-level Dedup (Gi\u1ea3m context th\u1eeba)\n",
            "        seen_articles = set()\n",
            "        deduped = []\n",
            "        for r in results:\n",
            "            article_id = r.format_relevant_article()\n",
            "            if article_id in seen_articles:\n",
            "                continue\n",
            "            seen_articles.add(article_id)\n",
            "            deduped.append(r)\n",
            "        results = deduped\n",
            "\n",
            "        # 6. Intent-aware (Query-aware) Filter\n",
            "        try:\n",
            "            import underthesea\n",
            "            tags = underthesea.pos_tag(query)\n",
            "            important_terms = [word.lower() for word, tag in tags if tag in ['N', 'Np', 'V', 'A', 'M'] and len(word) > 1]\n",
            "            if not important_terms:\n",
            "                important_terms = [w.lower() for w in query.split() if len(w) > 2]\n",
            "        except Exception:\n",
            "            important_terms = [w.lower() for w in query.split() if len(w) > 2]\n",
            "            \n",
            "        filtered_final = []\n",
            "        for r in results:\n",
            "            content_lower = r.content.lower()\n",
            "            # Chunk ph\u1ea3i ch\u1ee9a \u00edt nh\u1ea5t m\u1ed9t term quan tr\u1ecdng (intent keywords) t\u1eeb c\u00e2u h\u1ecfi\n",
            "            if any(term in content_lower for term in important_terms):\n",
            "                filtered_final.append(r)\n",
            "                \n",
            "        # Fallback n\u1ebfu filter qu\u00e1 ng\u1eb7t ngh\u00e8o drop h\u1ebft t\u1ea5t c\u1ea3 k\u1ebft qu\u1ea3\n",
            "        if not filtered_final and results:\n",
            "            filtered_final = results\n",
            "            \n",
            "        results = filtered_final[:k]\n",
            "            \n",
            "        elapsed = time.time() - t0\n",
            "        print(f\"[local] mode={mode} | rerank={rerank} | {len(results)} results | {elapsed:.2f}s\")\n",
            "        return results\n",
