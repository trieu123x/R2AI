            "                if any(kw in q_lower for kw in t_info[\"keywords\"]):\n",
            "                    active_topics.append(t_info[\"syns\"])\n",
            "                    \n",
            "            clauses = []\n",
            "            if active_subjects:\n",
            "                flat_subjects = []\n",
            "                for s in active_subjects:\n",
            "                    flat_subjects.extend(s)\n",
            "                flat_subjects = list(dict.fromkeys(flat_subjects))\n",
            "                clauses.append(\"(\" + \" OR \".join(flat_subjects) + \")\")\n",
            "                \n",
            "            if active_topics:\n",
            "                flat_topics = []\n",
            "                for t in active_topics:\n",
            "                    flat_topics.extend(t)\n",
