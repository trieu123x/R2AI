            "                r.best_chunk_content = r.content\n",
            "                \n",
            "                full_content = \"\\n...\\n\".join(expanded_articles[key])\n",
            "                r.content = full_content\n",
            "                r.source += \"_parent_expanded\"\n",
            "                final_results.append(r)\n",
            "            else:\n",
            "                final_results.append(r)\n",
            "                \n",
            "        return final_results\n",
