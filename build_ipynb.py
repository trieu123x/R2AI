            "        expanded_articles = {}\n",
            "        \n",
            "        for did, rows in doc_chunks.items():\n",
            "            current_article = None\n",
            "            for row in rows:\n",
            "                content = row[\"content\"] or \"\"\n",
            "                # Import extract_article_hint dynamically or rely on global\n",
            "                from retriever import extract_article_hint\n",
            "                hint = extract_article_hint(content)\n",
            "                if hint:\n",
            "                    current_article = hint\n",
            "                \n",
            "                if current_article and current_article in target_articles[did]:\n",
            "                    key = (did, current_article)\n",
            "                    \n",
            "                    # Windowing limit: Ch\u1ec9 g\u1ed9p n\u1ebfu chunk n\u1eb1m g\u1ea7n v\u1ecb tr\u00ed hit (+/- 2 chunks)\n",
            "                    # Ch\u1ed1ng ph\u00ecnh to context \u0111\u1ed1i v\u1edbi c\u00e1c \u0110i\u1ec1u omnibus (VD: \"s\u1eeda \u0111\u1ed5i, b\u1ed5 sung m\u1ed9t s\u1ed1 \u0111i\u1ec1u\")\n",
            "                    hits = hit_indexes.get(key, set())\n",
            "                    is_near_hit = False\n",
            "                    for h in hits:\n",
            "                        if abs(h - row[\"chunk_index\"]) <= 2:\n",
            "                            is_near_hit = True\n",
            "                            break\n",
            "                            \n",
            "                    if is_near_hit:\n",
            "                        if key not in expanded_articles:\n",
            "                            expanded_articles[key] = []\n",
