            "\n",
            "            fts_expr = \"\"\n",
            "            if clauses:\n",
            "                fts_expr = \" AND \".join(clauses)\n",
            "                print(f\"[local][FTS5] Structured query: {fts_expr}\")\n",
            "                results = _execute_fts_query(fts_expr, k)\n",
            "                if results:\n",
            "                    return results\n",
            "                print(f\"[local][FTS5] Structured query returned 0 results. Trying fallback OR...\")\n",
            "\n",
            "            # Fallback sang OR \u0111\u01a1n gi\u1ea3n c\u1ee7a c\u00e1c t\u1eeb kh\u00f3a c\u00f3 ngh\u0129a (lo\u1ea1i b\u1ecf stopword)\n",
            "            GRAMMAR_STOPWORDS = {\n",
            "                \"v\u00e0\", \"ho\u1eb7c\", \"nh\u01b0ng\", \"v\u00ec\", \"n\u00ean\", \"c\u1ee7a\", \"c\u00e1c\", \"nh\u1eefng\", \"l\u00e0\", \"c\u00f3\", \n",
            "                \"trong\", \"t\u1ea1i\", \"\u0111\u1ec3\", \"theo\", \"\u0111\u01b0\u1ee3c\", \"b\u1ecb\", \"cho\", \"ra\", \"v\u00e0o\", \"l\u00ean\", \n",
            "                \"xu\u1ed1ng\", \"do\", \"t\u1eeb\", \"\u0111\u1ebfn\", \"b\u1eb1ng\", \"with\", \"v\u1edbi\", \"v\u1ec1\", \"nh\u01b0\", \"th\u00ec\", \n",
            "                \"m\u00e0\", \"khi\", \"g\u00ec\", \"n\u00e0y\", \"\u0111\u00f3\", \"kia\", \"n\u1ecd\", \"th\u1ebf\", \"v\u1eady\", \"n\u00e0o\", \"ai\", \n",
            "                \"\u0111\u00e2u\", \"c\u00e1i\", \"con\", \"chi\u1ebfc\", \"n\u1ebfu\", \"s\u1ebd\", \"ph\u1ea3i\", \"sao\", \"th\u1ebf\"\n",
            "            }\n",
            "            words = [w.lower().strip() for w in query.split()]\n",
            "            terms = []\n",
            "            for w in words:\n",
            "                w = re.sub(r'[^\\w\\s\\u00C0-\\u024F\\u1E00-\\u1EFF]', '', w).strip()\n",
            "                if w and len(w) >= 3 and w not in GRAMMAR_STOPWORDS:\n",
            "                    terms.append(f'\"{w}\"')\n",
            "            \n",
            "            if terms:\n",
            "                fallback_expr = \" OR \".join(terms)\n",
            "                print(f\"[local][FTS5] Fallback OR query: {fallback_expr}\")\n",
            "                results = _execute_fts_query(fallback_expr, k)\n",
            "                if results:\n",
            "                    return results\n",
            "\n",
            "            print(\"[local][FTS5] 0 results from FTS5. Trying LIKE fallback...\")\n",
            "\n",
            "        # LIKE fallback search (slower, for queries without diacritics)\n",
            "        print(\"[local][FTS] Using LIKE fallback search...\")\n",
            "        keywords = [w for w in query.split() if len(w) >= 3][:5]\n",
            "        if not keywords:\n",
            "            return []\n",
            "\n",
            "        like_pattern = f\"%{keywords[0]}%\"\n",
            "        cur.execute(\"\"\"\n",
            "            SELECT id, document_id, chunk_index, content\n",
            "            FROM document_chunks\n",
            "            WHERE content LIKE ?\n",
            "            LIMIT ?;\n",
            "        \"\"\", (like_pattern, k * 3))\n",
            "        rows = cur.fetchall()\n",
            "\n",
            "        results = []\n",
            "        for row in rows:\n",
            "            cid, doc_id, chunk_idx, content = row[0], row[1], row[2], row[3] or \"\"\n",
            "            score = float(sum(1 for kw in keywords if kw in content))\n",
            "            if score == 0:\n",
            "                continue\n",
            "            meta = _parse_meta_from_content(content)\n",
            "            results.append(RetrievalResult(\n",
            "                chunk_id=str(cid),\n",
            "                document_id=doc_id,\n",
            "                chunk_index=chunk_idx,\n",
            "                content=content,\n",
            "                doc_number=meta[\"document_number\"],\n",
            "                title=meta[\"title\"],\n",
