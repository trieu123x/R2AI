            "    def expand_query(self, query: str) -> str:\n",
            "        \"\"\"Query Expansion b\u1eb1ng rule-based dictionary.\"\"\"\n",
            "        q_lower = query.lower()\n",
            "        expanded_terms = []\n",
            "        \n",
            "        intents = {\n",
            "            \"h\u1ed7 tr\u1ee3\": [\"\u01b0u \u0111\u00e3i\", \"ch\u00ednh s\u00e1ch h\u1ed7 tr\u1ee3\", \"khuy\u1ebfn kh\u00edch\"],\n",
            "            \"\u01b0u \u0111\u00e3i\": [\"mi\u1ec5n\", \"gi\u1ea3m\", \"h\u1ed7 tr\u1ee3\"],\n",
