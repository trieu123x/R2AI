            "            # Normalize PhoRanker output to positive if it outputs logits\n",
            "            if rerank_score < 0 or rerank_score > 1.0:\n",
            "                import math\n",
            "                rerank_score = 1 / (1 + math.exp(-max(min(rerank_score, 100), -100)))\n",
            "                \n",
            "            weight = LEGAL_WEIGHT.get(r.legal_type, 0.7)\n",
            "            final_score = rerank_score * weight\n",
            "            \n",
            "            r.score = final_score\n",
            "            r.source = f\"rerank({r.source})\"\n",
            "            \n",
            "        results.sort(key=lambda x: x.score, reverse=True)\n",
            "        \n",
            "        # L\u1ecdc MMR Diversity: max 2 chunk m\u1ed7i document\n",
            "        filtered_results = []\n",
            "        doc_count = {}\n",
            "        for r in results:\n",
            "            doc_id = r.document_id\n",
            "            if doc_count.get(doc_id, 0) < 2:\n",
            "                filtered_results.append(r)\n",
            "                doc_count[doc_id] = doc_count.get(doc_id, 0) + 1\n",
            "        \n",
            "        if top_k:\n",
            "            return filtered_results[:top_k]\n",
            "        return filtered_results\n",
            "\n",
            "    def are_chunks_duplicate(self, c1: RetrievalResult, c2: RetrievalResult) -> bool:\n",
            "        \"\"\"Ki\u1ec3m tra xem hai chunk c\u00f3 tr\u00f9ng l\u1eb7p n\u1ed9i dung \u0111\u00e1ng k\u1ec3 hay kh\u00f4ng.\"\"\"\n",
            "        if c1.document_id == c2.document_id and c1.chunk_index == c2.chunk_index:\n",
            "            return True\n",
            "            \n",
            "        # Tr\u00edch xu\u1ea5t ph\u1ea7n n\u1ed9i dung th\u1ef1c t\u1ebf \u0111\u1ec3 so s\u00e1nh\n",
            "        body1 = c1.content.split(\"| N\u1ed9i dung:\")[-1].strip()\n",
            "        body2 = c2.content.split(\"| N\u1ed9i dung:\")[-1].strip()\n",
            "        \n",
            "        def normalize(text):\n",
            "            text = text.lower()\n",
            "            text = re.sub(r'[^\\w\\s]', '', text)\n",
            "            return set(text.split())\n",
            "            \n",
            "        words1 = normalize(body1)\n",
            "        words2 = normalize(body2)\n",
            "        if not words1 or not words2:\n",
            "            return False\n",
            "            \n",
            "        intersection = words1.intersection(words2)\n",
            "        union = words1.union(words2)\n",
            "        jaccard = len(intersection) / len(union)\n",
            "        \n",
            "        # Jaccard score cao => tr\u00f9ng l\u1eb7p\n",
            "        if jaccard > 0.8:\n",
            "            return True\n",
            "            \n",
            "        # N\u1ebfu tr\u00f9ng \u0110i\u1ec1u/Kho\u1ea3n lu\u1eadt v\u00e0 \u0111\u1ed9 t\u01b0\u01a1ng \u0111\u1ed3ng t\u01b0\u01a1ng \u0111\u1ed1i cao => tr\u00f9ng l\u1eb7p (v\u00ed d\u1ee5 gi\u1eefa Lu\u1eadt v\u00e0 VBHN)\n",
            "        if c1.article_hint and c2.article_hint and c1.article_hint == c2.article_hint:\n",
            "            if \"\u0111i\u1ec1u\" in c1.article_hint.lower() or \"kho\u1ea3n\" in c1.article_hint.lower():\n",
            "                if jaccard > 0.6:\n",
            "                    return True\n",
            "        return False\n",
            "\n",
            "    # \u2500\u2500 Entry point \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n",
            "\n",
