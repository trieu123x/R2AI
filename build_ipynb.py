            "            doc_text = f\"V\u0103n b\u1ea3n: {r.legal_type} {r.doc_number} - {r.title}\\n{article_str}N\u1ed9i dung:\\n{best_text}\"\n",
            "            \n",
            "            pairs.append([query, doc_text])\n",
            "            \n",
            "        t_rerank = time.time()\n",
            "        scores = self._reranker.predict(pairs, show_progress_bar=False)\n",
            "        print(f\"[local] Reranking took {time.time() - t_rerank:.2f}s for {len(pairs)} pairs.\")\n",
            "        \n",
            "        LEGAL_WEIGHT = {\n",
            "            \"Lu\u1eadt\": 1.0,\n",
            "            \"B\u1ed9 lu\u1eadt\": 1.0,\n",
            "            \"Ngh\u1ecb quy\u1ebft\": 0.95,\n",
            "            \"Ngh\u1ecb \u0111\u1ecbnh\": 0.85,\n",
            "            \"Th\u00f4ng t\u01b0\": 0.75,\n",
